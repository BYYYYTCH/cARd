public class Cube {
    float[] color;
    double x;
    double y;
    double z;
    double vx;
    double vy;
    double vz;
    public Cube(float[] color,double x, double y,double z,double vx,double vy,double vz){
        this.color = color;
        this.x = x;
        this.y = y;
        this.z = z;
        this.vx = vx;
        this.vy = vy;
        this.vz = vz;
    }
}
